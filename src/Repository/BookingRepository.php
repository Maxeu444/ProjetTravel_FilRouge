<?php

namespace App\Repository;

use App\Entity\Booking;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Booking>
 *
 * @method Booking|null find($id, $lockMode = null, $lockVersion = null)
 * @method Booking|null findOneBy(array $criteria, array $orderBy = null)
 * @method Booking[]    findAll()
 * @method Booking[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class BookingRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Booking::class);
    }

    public function save(Booking $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Booking $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function calculateSeatNumber($plane_capacity, $flight)
    {
        // récupère les Bookings liées au du vol
        $bookings = $this->findBy(["flight_id" => $flight]);
        $placesAlreadyTaken = [];
        foreach ($bookings as $booking) {
            array_push($placesAlreadyTaken, $booking->getSeatNumber());
        }
        // défini un numéro de siège libre
        do {
            $seatNumber = rand(1, $plane_capacity);
        } while (in_array($seatNumber, $placesAlreadyTaken));
        return $seatNumber;
    }

